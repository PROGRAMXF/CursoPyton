"""
escribir un programa para sacar el promedio de tres notas de estudiantes

"""

nota1 = float(input("Ingresar la primer nota: "))
nota2 = float(input("Ingresar la segunda nota: "))
nota3 = float(input("Ingresar la tercer nota: "))

prom = (nota1 + nota2 + nota3) / 3

print("El promedio de las tres notas del estudiante es: ", prom)